
/*
Variable:
A variable is a named container to store data like numbers, text, boolean, arrays, objects..

Typescript supports 3 keywords to declare the variables
1. var
2. let
3. const

syntax:
keyword variableName:dataType=value;

Primitive data types:
number --> to store numbers (integers and floating point values)
string --> to store text values
boolean --> to store either true/false

Naming conventions:
variable name should starts with letter, _ or $
we can not use reserved keywords as variable names.. class, interface, const, var, let, export, import....

examples:
var name:string="testuser";
let count:number=10;
const isVisible:boolean=true;

*/

