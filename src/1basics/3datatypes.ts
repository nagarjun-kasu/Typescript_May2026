//Data Types

//string: To store string values

let companyName:string = "Test Company";
console.log(companyName);

//number: To store numbers (numeric and non-numeric)
let count:number=30;
console.log(count);

//boolean: To store either true or false
let isVisible:boolean=true;
console.log(isVisible);

//string literal

let name:string="Nagarjun";
console.log(`Hello ${name}, welcome to typescript training...`); //Hello Nagarjun, welcome to typescript training...